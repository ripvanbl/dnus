'use strict';
var i = 0;


////////////////////////////////////////////////////////////
// Module Definition
////////////////////////////////////////////////////////////


/**
 * Provides an overly simple number incrementer.
 * @module incrementer
 */
module.exports = {
  /**
    * Increments the singleton number by 1.
    * @return {int}
    */
  next: next
};





////////////////////////////////////////////////////////////
// Executors
////////////////////////////////////////////////////////////


function next() {
  i++;
  return i;
}
